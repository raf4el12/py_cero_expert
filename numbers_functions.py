import math


print(round(1.3)) # lo que hace la funcion round es redondear al numero mas cercano
print(abs(-2))  # lo que hace la funcion abs es sacar un valor absoluto es decir simpre lo sacara como un numero entero


print(math.ceil(1.1)) # va a tomar el numero mas cercano hacia arriba es decir el siguiente numero seria 2
print(math.floor(2.99999)) # el numero hacia abajo 
print(math.isnan(23))
#print(math.isnan("HELLO")) # isnan solo nos servira si solo le pasamos numeros reales en INT

print(math.pow(10,3)) # se leeria 10 elevado a la 3

print(math.sqrt(9)) # hara sacar la raiz cuadrada de tal numero que le pases funcion SQRT

